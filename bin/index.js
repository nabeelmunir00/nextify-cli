// ─── Create Files/Folders ──────────────────────────────────────────────────────
function createStructure(structure, spinner) {
  let filesCreated = 0;
  let foldersCreated = 0;

  for (const item of structure) {
    const fullPath = path.resolve(process.cwd(), item);

    if (item.endsWith("/")) {
      // It's a folder
      if (!fs.existsSync(fullPath)) {
        fs.mkdirSync(fullPath, { recursive: true });
        foldersCreated++;
      }
    } else {
      // It's a file
      const dir = path.dirname(fullPath);
      if (!fs.existsSync(dir)) {
        fs.mkdirSync(dir, { recursive: true });
      }

      if (!fs.existsSync(fullPath)) {
        const fileName = path.basename(fullPath);
        const content = templates[fileName] || "";
        fs.writeFileSync(fullPath, content);
        filesCreated++;
      }
    }

    spinner.text = chalk.gray(`Creating: ${item}`);
  }

  return { filesCreated, foldersCreated };
}

// ─── CLI Commands ──────────────────────────────────────────────────────────────
program
  .name("next-scaffold")
  .description("⚡ Quickly scaffold a Next.js project structure")
  .version("1.0.0");

program
  .command("create <project-name>")
  .description("Create a new Next.js folder structure")
  .option("--api", "Include API routes folder")
  .option("--auth", "Include auth pages (login/register)")
  .option("--dashboard", "Include dashboard route group")
  .option("--full", "Include everything (api + auth + dashboard)")
  .action((projectName, options) => {
    if (options.full) {
      options.api = true;
      options.auth = true;
      options.dashboard = true;
    }

    console.log(chalk.yellow(`\n📁 Creating project: ${chalk.bold(projectName)}\n`));

    const structure = getStructure(projectName, options);
    const spinner = ora("Setting up folder structure...").start();

    try {
      const { filesCreated, foldersCreated } = createStructure(structure, spinner);

      spinner.succeed(chalk.green("Structure created successfully!\n"));

      console.log(chalk.cyan("📊 Summary:"));
      console.log(chalk.white(`   📂 Folders: ${foldersCreated}`));
      console.log(chalk.white(`   📄 Files:   ${filesCreated}\n`));

      console.log(chalk.cyan("🚀 Next steps:\n"));
      console.log(chalk.white(`   cd ${projectName}`));
      console.log(chalk.white("   npm install"));
      console.log(chalk.white("   npm run dev\n"));

      console.log(chalk.gray("Happy coding! 🎉\n"));
    } catch (err) {
      spinner.fail(chalk.red("Something went wrong!"));
      console.error(chalk.red(err.message));
      process.exit(1);
    }
  });

program
  .command("preview <project-name>")
  .description("Preview the folder structure without creating files")
  .option("--api", "Include API routes")
  .option("--auth", "Include auth pages")
  .option("--dashboard", "Include dashboard")
  .option("--full", "Include everything")
  .action((projectName, options) => {
    if (options.full) {
      options.api = true;
      options.auth = true;
      options.dashboard = true;
    }

    const structure = getStructure(projectName, options);

    console.log(chalk.yellow(`\n📁 Preview for: ${chalk.bold(projectName)}\n`));

    for (const item of structure) {
      if (item.endsWith("/")) {
        console.log(chalk.cyan(`  📂 ${item}`));
      } else {
        console.log(chalk.white(`  📄 ${item}`));
      }
    }

    console.log(
      chalk.gray(`\n  Total: ${structure.filter((i) => i.endsWith("/")).length} folders, ${structure.filter((i) => !i.endsWith("/")).length} files\n`)
    );
  });

program.parse(process.argv);
